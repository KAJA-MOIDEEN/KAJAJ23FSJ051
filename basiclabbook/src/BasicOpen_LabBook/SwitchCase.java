package BasicOpen_LabBook;

public class SwitchCase {

	public static void main(String[] args) {
		int tech = 2;
		switch (tech) {
		case 1: {
			System.err.println("java");
			break;
		}
		case 2: {
			System.out.println("ES6");
			break;
		}
		case 3: {
			System.out.println("Not Listed");
		}
		}
	}

}
