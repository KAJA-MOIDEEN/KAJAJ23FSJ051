package basic_ClosedLabBook;
/*3. Write a program to declare all primitive data types with all possible types of
initialization and also check implicit and explicit type casting by assigning
them to each other.*/
public class DefaultValueOfPrimitiveType {
	
	static byte b;
	static short s;
	static int i;
	static long l;
	static float f;
	static double d;
	static boolean bool;
	
	public static void main(String[] args) {
		
		System.out.println("default value of byte is : " + b);
		System.out.println("default value of short is : " + s);
		System.out.println("default value of int is : " + i);
		System.out.println("default value of long is : " + l);
		System.out.println("default value of float is : " + f);
		System.out.println("default value of double is : " + d);
		System.out.println("default value of boolean is : " + bool);
	}

}
